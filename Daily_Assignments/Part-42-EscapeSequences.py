# To tab indent a line, use \t
print("\tThis line has been indented.")

# To start a string on a new line use \n
print("This string is on a line \nbefore this other line")

# To create a carriage return, use \r
print("\nTHIS LINE WON'T SHOW UP!  \rThis other string is on a new line. \n\tAnd this string was started on a third line, indented with a tab")